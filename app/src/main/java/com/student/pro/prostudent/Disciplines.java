public class Disciplines {
    String name,year,tag;

    public Disciplines(String name, String year, String tag) {
        this.name = name;
        this.year = year;
        this.tag = tag;
    }

    public String getName() {
        return name;
    }

    public String getYear() {
        return year;
    }

    public String getTag() {
        return tag;
    }

}
